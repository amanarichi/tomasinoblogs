                    <b>Religous Celebrations</b> 
                    <br/>
                    The chapel of the University of Santo Tomas in which the spiritual, moral, pastoral needs of the university people are cared for, and where official religious where parishioners go for their sacramental needs like weddings, baptisms, funerals, confessions, masses and others. It is placed under the care of the religious community of the Dominican Order. On April 26 1942, the parish was canonically inaugurated with a pontifical high mass celebrated by Msgr. O’Doherty.
                    <br/>
                    <br/>
                    <b><u><center>Historical and Symbolic Importance</center></u></b>
                    </br>
                    On September 28 1942, the image of the ‘La Naval de Manila was transferred from Santo Domingo Church, Intramuros to UST and installed in the Parish Church of Santisimo Rosario to save it from heavy bombings in Intramuros. That year, the Novena to Our Lady of La Naval was held for the first time in the parish church. The novena became a tradition and henceforth, the parish fiesta began being celebrated every 7th day of October. After 12 years of stay at the parish church, the image of La Naval de Manila was transferred to Santo Domingo Church in Quezon City on October 10 1954. It was then that the original parish image was restored to its former place at the main altar. 

        <br/>
        <br/>
        To give way to the celebration of the feast of ‘La Naval de Manila’ in Santo Domingo Church every second Sunday of October, the Feast of the Santisimo Rosario was changed to the first Sunday of October. In its seventy (70) years of existence, and with the parish population of 56 850, the parish was privileged to have the following as parish priests:
